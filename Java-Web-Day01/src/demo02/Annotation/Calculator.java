package demo02.Annotation;

public class Calculator {
    /**
     *
     * @param a a param is a number
     * @param b b param is a number
     * @return return a number
     */
    public int sum (int a, int b){
        return a+b;
    }

    /**
     *
     * @param a a param is a number
     * @param b b param is a number
     * @return return a number
     */
    public int time (int a,int b){
        return a * b;
    }
}
